   <p class="mb-4">
        Un bilan carbone est une méthode permettant de comptabiliser les émissions de gaz à effet de serre (GES) d'une activité, d'une organisation ou d'un individu. Il s'agit d'évaluer l'impact environnemental en termes de tonnes de CO₂ émises directement ou indirectement.
      </p>
      <p class="mb-4">
            L'effet de serre est un phénomène naturel qui se produit lorsque certains gaz dans l'atmosphère piégeant la chaleur du soleil, contribuant ainsi à réchauffer la surface de la Terre. Cependant, l'augmentation des émissions de GES due aux activités humaines intensifie cet effet, entraînant des changements climatiques tels que le réchauffement global, les tempêtes plus violentes et les phénomènes météorologiques extrêmes.
        </p>
      <h2 class="text-2xl font-bold mb-4">Pourquoi le calculer ?</h2>
      <p class="mb-4">
